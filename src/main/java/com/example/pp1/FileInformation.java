package com.example.pp1;

public class FileInformation {
    public String inputExpanse;
    public String outputExpanse;
    public String outputName;
    public int cipher;

    public void setInputExpanse(String inputExpanse) {
        this.inputExpanse = inputExpanse;
    }

    public void setOutputExpanse(String outputExpanse) {
        this.outputExpanse = outputExpanse;
    }

    public void setOutputName(String outputName) {
        this.outputName = outputName;
    }

    public void setCipher(int cipher) {
        this.cipher = cipher;
    }
}
